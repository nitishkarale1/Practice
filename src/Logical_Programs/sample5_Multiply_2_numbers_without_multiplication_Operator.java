package Logical_Programs;

public class sample5_Multiply_2_numbers_without_multiplication_Operator
{
	public static void main(String[] args) 
	{
		int num1=5;
		int num2=30;
		int sum=0;  //150
				//	 5<=5
		for(int i=1; i<=num1; i++)
		{
			sum=sum+num2;  //150
		}
		System.out.println(sum);
		
	}
}
